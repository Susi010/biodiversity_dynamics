Fitis / Willow Warbler (Phylloscopus trochilus)

European breeding population 62 - 97 million pairs (BirdLife International 2015), ca. 30% of global range

moderate decline in europe between 1980 and 2013 (EBCC 2015) due to drought conditions in wintering quarters, habitat alterations (Thingstad et al. 2014), declining oak tree health impacting major food source (Peach et al. 1995)


Gartengrasmücke / Garden Warbler (Sylvia borin)

population 41 - 68 million mature individuals

European breeding population 17 - 27 million pairs

moderate decline in Europe between 1980 and 2013 (EBCC 2015)

Alterations or fragmentation of important refuelling habitats south of the Sahara could reduce survival (Aymí and Gargallo 2015)


Schilfrohrsänger / Sedge Warbler (Acrocephalus schoenobaenus)

population 12 - 22 million mature individuals

european breeding population 3,8 - 6,4 million pairs


Drosselrohrsänger / Great Reed-warbler (Acrocephalus arundinaceus)

european breeding population 2,6 - 4,6 milllion pairs

declining trend may be due to climate change, habitat loss; Drainage and irrigation, decreasing reed quality, lower arthropod densities (Dyrcz 2006)


Teichrohrsänger / Common Reed-warbler (Acrocephalus scirpaceus)

population 12 - 23 million adult individuals

european breeding population 2 - 4 milllion pairs

local declines due to habitat destruction, reedbed die-back, caused partly by eutrophication (Hagemeijer and Blair 1997), reclamation of marshland (Dyrcz et al. 2015)


#Rohrschwirl / Savi's Warbler (Locustella luscinioides)

population 0,8 - 1,5 million adult individuals

european breeding population 0,28 - 0,5 milllion pairs

known for short term population fluctuations may be from habitat changes, natural changes in water level and weather conditions in African winter quarters (Hagemeijer and Blair 1997)


Bartmeise / Bearded Reedling (Panurus biarmicus)

population 3 - 6 million adult individuals

european breeding population 0,23 - 0,45 milllion pairs

local declines Turkey, Netherland due to habitat loss (Robson 2015), (Hagemeijer and Blair 1997), (Burton and Burton 2002)



Blaumeise / Eurasian Blue Tit (Cyanistes caeruleus)

population 20 - 44 million adult individuals

european breeding population 2,8 - 4,3 milllion pairs

increasing following recorded range expansions (del Hoyo et al. 2007), high annual and local fluctuations driven by habitat carrying capacity, breeding success, winter mortality (Hagemeijer and Blair 1997) and competition from Parus major (Gosler et al. 2013)


Weißsterniges Blaukehlchen / Bluethroat (Luscinia svecica)

population 35 - 65 million adult individuals

european breeding population 4,5 - 8 milllion pairs

pronounced, but largely unexplained, population fluctuations (Hagemeijer and Blair 1997, Collar 2015)


Amsel / Eurasian Blackbird (Turdus merula)

population 10 - 500 million adult individuals

european breeding population 55 - 87 milllion pairs


Feldsperling / Eurasian Tree Sparrow (Passer montanus)

population 190 - 310 million adult individuals

European breeding population 55 - 87 milllion pairs

declines in Europe may be result of increased use of pesticides and herbicides and autumn sowing of cereals (Hagemeijer and Blair 1997)


Goldammer / Yellowhammer (Emberiza citrinella)

population 40 - 70 million adult individuals

European breeding population 13 - 20 milllion pairs

decline owing to ongoing habitat destruction and reduction in cultivation of cereal crops



Rohrammer / Reed Bunting (Emberiza schoeniclus)

population 20 - 40 million adult individuals

European breeding population  4 - 7 milllion pairs

decline in Europe due to agricultural intensification (Vera et al. 2014)